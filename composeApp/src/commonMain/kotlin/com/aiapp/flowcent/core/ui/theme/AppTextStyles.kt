package com.aiapp.flowcent.core.ui.theme

import androidx.compose.runtime.Composable
import androidx.compose.ui.text.TextStyle
import androidx.compose.ui.unit.sp

@Composable
fun mediumWhite18(): TextStyle {
    return TextStyle(
        color = white,
        fontSize = 18.sp,
    )
}

@Composable
fun bold18Black(): TextStyle {
    return TextStyle(
        color = text_black,
        fontSize = 18.sp,
    )
}

@Composable
fun medium14Black(): TextStyle {
    return TextStyle(
        color = text_black,
        fontSize = 14.sp,
    )
}